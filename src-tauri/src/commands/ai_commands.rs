/// Check if a saved session exists for the given workspace
#[tauri::command]
pub fn has_saved_session(workspace_path: String) -> bool {
    let path = Path::new(&workspace_path).join(SESSION_FILE);
    path.exists()
}

/// Restore a saved session from the workspace file
#[tauri::command]
pub fn restore_ai_session(
    state: tauri::State<'_, ConversationState>,
    workspace_path: String,
) -> Result<usize, String> {
    let saved = load_session_from_file(&workspace_path)
        .ok_or("No saved session found")?;

    let mut messages = state.messages.lock().map_err(|e| e.to_string())?;
    let mut sp = state.system_prompt.lock().map_err(|e| e.to_string())?;
    let mut wp = state.workspace_path.lock().map_err(|e| e.to_string())?;
    let mut pv = state.provider.lock().map_err(|e| e.to_string())?;

    let count = saved.messages.len();
    *messages = saved.messages;
    *sp = saved.system_prompt;
    *wp = workspace_path;
    *pv = saved.provider;

    Ok(count)
}

/// Clear the saved session file for a workspace
#[tauri::command]
pub fn clear_saved_session(workspace_path: String) -> Result<(), String> {
    let path = Path::new(&workspace_path).join(SESSION_FILE);
    if path.exists() {
        std::fs::remove_file(&path).map_err(|e| format!("Delete error: {}", e))?;
    }
    Ok(())
}

