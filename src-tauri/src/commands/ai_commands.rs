/// Phase 2: Send a student message to the Teaching Agent.
/// Uses the lesson_brief from Prep Phase. Falls back to legacy if no brief.
#[tauri::command]
pub async fn send_teaching_message(
    app: AppHandle,
    state: tauri::State<'_, ConversationState>,
    payload: TeachingPayload,
) -> Result<(), String> {
    let workspace_path = state.workspace_path.lock().map_err(|e| e.to_string())?.clone();
    let provider = state.provider.lock().map_err(|e| e.to_string())?.clone();
    let model = state.model.lock().map_err(|e| e.to_string())?.clone();
    let lesson_brief = state.lesson_brief.lock().map_err(|e| e.to_string())?.clone();

    // Use teaching-specific prompt or fall back to session prompt
    let system_prompt = match payload.teaching_prompt {
        Some(ref p) if !p.is_empty() => p.clone(),
        _ => {
            let tp = state.teaching_prompt.lock().map_err(|e| e.to_string())?.clone();
            if tp.is_empty() {
                state.system_prompt.lock().map_err(|e| e.to_string())?.clone()
            } else {
                tp
            }
        }
    };

    // Add user message, with periodic Iron Law reminder injection
    let current_messages = {
        let messages = state.messages.lock().map_err(|e| e.to_string())?;
        let message_count = messages.len();

        // Every 10 messages (~5 teaching turns), inject a hidden reminder
        // to reinforce the Three Iron Laws (combats long-context drift)
        let user_text = if message_count > 0 && message_count % 10 == 0 {
            format!(
                "[SYSTEM REMINDER — not visible to student, for your internal calibration only]\n\
                三铁律自检：\n\
                □ 只问不说：你上一条回复有\"解释\"吗？问句 ≥ 陈述句了吗？\n\
                □ 眼睛看学生：你的问题是从学生上一句话长出来的吗？盲测C通过了吗？\n\
                □ 答案是学生的：你有没有替学生总结、命名、下结论？\n\
                □ 最后一英里：学生接近答案时，追问\"你怎么理解这个词？\"而非确认。\n\
                现在回到学生的消息：\n\n{}",
                payload.text
            )
        } else {
            payload.text.clone()
        };

        let user_msg = Message {
            role: "user".to_string(),
            content: vec![ContentBlock::Text { text: user_text }],
        };

        let mut msgs = messages.clone();
        msgs.push(user_msg);
        msgs
    };

    // If we have a lesson_brief, use the new teaching turn; otherwise fall back to legacy
    let result = if let Some(ref brief) = lesson_brief {
        runtime::run_teaching_turn(
            &app,
            &payload.api_key,
            &provider,
            &model,
            &workspace_path,
            &system_prompt,
            brief,
            current_messages,
        ).await
    } else {
        println!("[Warning] No lesson_brief — falling back to legacy agent turn");
        runtime::run_agent_turn(
            &app,
            &payload.api_key,
            &provider,
            &model,
            &workspace_path,
            &system_prompt,
            current_messages,
        ).await
    };

    match result {
        Ok(updated_messages) => {
            let mut messages = state.messages.lock().map_err(|e| e.to_string())?;
            *messages = updated_messages;

            // Persist session
            let _ = save_session_to_file(
                &workspace_path,
                &SavedSession {
                    messages: messages.clone(),
                    system_prompt: system_prompt.clone(),
                    provider: provider.clone(),
                    model: model.clone(),
                },
            );

            Ok(())
        }
        Err(e) => {
            use tauri::Emitter;
            let _ = app.emit("agent-event", crate::ai::types::AgentEvent::Error {
                message: e.clone(),
            });
            let _ = app.emit("agent-event", crate::ai::types::AgentEvent::TurnComplete);
            Err(e)
        }
    }
}

