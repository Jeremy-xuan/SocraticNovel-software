/// Phase 1: Run the Prep Agent to generate a lesson brief.
/// Must be called after start_ai_session. Stores the lesson_brief in state.
#[tauri::command]
pub async fn run_prep_phase(
    app: AppHandle,
    state: tauri::State<'_, ConversationState>,
    payload: PrepPhasePayload,
) -> Result<String, String> {
    let workspace_path = state.workspace_path.lock().map_err(|e| e.to_string())?.clone();
    let provider = state.provider.lock().map_err(|e| e.to_string())?.clone();
    let model = state.model.lock().map_err(|e| e.to_string())?.clone();

    let system_prompt = match payload.system_prompt {
        Some(ref p) if !p.is_empty() => p.clone(),
        _ => state.system_prompt.lock().map_err(|e| e.to_string())?.clone(),
    };

    let initial_message = "请按照课前准备流程，读取 workspace 文件并生成 lesson_brief。".to_string();

    let (brief, _prep_messages) = runtime::run_prep_phase(
        &app,
        &payload.api_key,
        &provider,
        &model,
        &workspace_path,
        &system_prompt,
        &initial_message,
    ).await?;

    // Store lesson_brief in state
    {
        let mut lb = state.lesson_brief.lock().map_err(|e| e.to_string())?;
        *lb = Some(brief.clone());
    }

    Ok(brief)
}

