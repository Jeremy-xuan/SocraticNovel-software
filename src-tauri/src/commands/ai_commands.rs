// ─── Note Generation Commands ─────────────────────────────────────

#[derive(Deserialize)]
#[serde(rename_all = "camelCase")]
pub struct GenerateNotesPayload {
    pub api_key: String,
}

/// Generate structured review notes from the current conversation.
/// Returns the notes as a Markdown string.
#[tauri::command]
pub async fn generate_lesson_notes(
    state: tauri::State<'_, ConversationState>,
    payload: GenerateNotesPayload,
) -> Result<String, String> {
    let provider = state.provider.lock().map_err(|e| e.to_string())?.clone();
    let model = state.model.lock().map_err(|e| e.to_string())?.clone();
    let messages = state.messages.lock().map_err(|e| e.to_string())?.clone();

    if messages.is_empty() {
        return Err("没有对话记录可以生成笔记".to_string());
    }

    runtime::generate_notes(&payload.api_key, &provider, &model, &messages).await
}

