const SESSION_FILE: &str = ".app_session.json";

/// Conversation state held in Tauri's managed state
pub struct ConversationState {
    pub messages: Mutex<Vec<Message>>,
    pub system_prompt: Mutex<String>,
    pub workspace_path: Mutex<String>,
    pub provider: Mutex<String>,
    pub model: Mutex<String>,
    /// Lesson brief generated by Prep Phase (multi-agent)
    pub lesson_brief: Mutex<Option<String>>,
    /// Teaching-specific system prompt (loaded from split config files)
    pub teaching_prompt: Mutex<String>,
    /// Practice-specific system prompt
    pub practice_prompt: Mutex<String>,
    /// Meta Prompt system prompt (for workspace generation flow)
    pub meta_prompt_prompt: Mutex<String>,
}

impl Default for ConversationState {
    fn default() -> Self {
        Self {
            messages: Mutex::new(Vec::new()),
            system_prompt: Mutex::new(String::new()),
            workspace_path: Mutex::new(String::new()),
            provider: Mutex::new("anthropic".to_string()),
            model: Mutex::new(String::new()),
            lesson_brief: Mutex::new(None),
            teaching_prompt: Mutex::new(String::new()),
            practice_prompt: Mutex::new(String::new()),
            meta_prompt_prompt: Mutex::new(String::new()),
        }
    }
}

/// Serializable session state for persistence
#[derive(Serialize, Deserialize)]
struct SavedSession {
    messages: Vec<Message>,
    system_prompt: String,
    provider: String,
    #[serde(default)]
    model: String,
}

fn save_session_to_file(workspace_path: &str, session: &SavedSession) -> Result<(), String> {
    if workspace_path.is_empty() {
        return Ok(());
    }
    let path = Path::new(workspace_path).join(SESSION_FILE);
    let json = serde_json::to_string(session).map_err(|e| format!("Serialize error: {}", e))?;
    std::fs::write(&path, json).map_err(|e| format!("Write error: {}", e))?;
    Ok(())
}

fn load_session_from_file(workspace_path: &str) -> Option<SavedSession> {
    let path = Path::new(workspace_path).join(SESSION_FILE);
    let json = std::fs::read_to_string(&path).ok()?;
    serde_json::from_str(&json).ok()
}

