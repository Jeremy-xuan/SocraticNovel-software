/// Generate a summary from conversation messages for the post-lesson agent
fn generate_conversation_summary(messages: &[Message]) -> String {
    let mut summary = String::from("=== 课堂对话记录 ===\n\n");
    for msg in messages {
        let role_label = match msg.role.as_str() {
            "user" => "学生",
            "assistant" => "老师(内部)",
            _ => &msg.role,
        };
        for block in &msg.content {
            match block {
                ContentBlock::Text { text } => {
                    if !text.trim().is_empty() {
                        summary.push_str(&format!("[{}] {}\n\n", role_label, text.chars().take(500).collect::<String>()));
                    }
                }
                ContentBlock::ToolUse { name, input, .. } => {
                    if name == "respond_to_student" {
                        if let Some(content) = input["content"].as_str() {
                            summary.push_str(&format!("[老师→学生] {}\n\n", content));
                        }
                    }
                }
                ContentBlock::ToolResult { content, .. } => {
                    // Skip tool results in summary
                    let _ = content;
                }
                ContentBlock::Image { .. } => {
                    // Skip images in summary
                }
            }
        }
    }
    summary
}

// ─── Stateless simple chat (for world-building, etc.) ───────────────

#[derive(Deserialize)]
#[serde(rename_all = "camelCase")]
pub struct SimpleChatMessage {
    pub role: String,
    pub text: String,
}

#[derive(Deserialize)]
#[serde(rename_all = "camelCase")]
pub struct SimpleChatPayload {
    pub system_prompt: String,
    pub messages: Vec<SimpleChatMessage>,
    pub provider: String,
    pub model: String,
    pub api_key: String,
}

/// Stateless non-streaming chat — no session, no tools, no history management.
#[tauri::command]
pub async fn simple_chat(payload: SimpleChatPayload) -> Result<String, String> {
    let messages: Vec<Message> = payload
        .messages
        .into_iter()
        .map(|m| Message {
            role: m.role,
            content: vec![ContentBlock::Text { text: m.text }],
        })
        .collect();

    let (content_blocks, _stop) = match payload.provider.as_str() {
        "anthropic" => {
            let client = ClaudeClient::with_model(payload.api_key, &payload.model);
            client
                .send_message(&payload.system_prompt, messages, None)
                .await?
        }
        "openai" | "deepseek" | "google" | "github" => {
            let client =
                OpenAiClient::with_model(payload.api_key, &payload.provider, &payload.model);
            client
                .send_message(&payload.system_prompt, messages, None)
                .await?
        }
        _ => return Err(format!("Unsupported provider: {}", payload.provider)),
    };

    let text = content_blocks
        .iter()
        .filter_map(|b| match b {
            ContentBlock::Text { text } => Some(text.as_str()),
            _ => None,
        })
        .collect::<Vec<_>>()
        .join("");

    Ok(text)
}
